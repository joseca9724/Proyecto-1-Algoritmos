    private void btnIngresarPeliculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarPeliculaActionPerformed

        Pelicula pelicula=new Pelicula(jtfCodigo.getText(),jtfNombre.getText(),jtfGenero.getText(),jtfCantidad.getText(),jtfSubtitulos.getText(),jtfPremier.getText());
        for(int i=0;i<vectorListas.length;i++){
            if(vectorListas[i].getGenero().equalsIgnoreCase(pelicula.getGenero())){
                vectorListas[i].ingresarPelicula(pelicula);
                break;
            }
        }
        JOptionPane.showMessageDialog(rootPane, "Pelicula ingresada");

    }//GEN-LAST:event_btnIngresarPeliculaActionPerformed

