    private void btnEscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEscribirActionPerformed
        Pelicula pelicula=new Pelicula(jtfCodigo.getText(),jtfNombre.getText(),jtfGenero.getText(),jtfCantidad.getText(),jtfSubtitulos.getText(),jtfPremier.getText());
        String[] nextLine={jtfCodigo.getText(),jtfNombre.getText(),jtfGenero.getText(),jtfCantidad.getText(),jtfSubtitulos.getText(),jtfPremier.getText()};
        try {
            CSVWriter file=new CSVWriter(new FileWriter(this.ruta));
            
            file.writeNext(nextLine);
            file.close();
        } catch (IOException ex) {
            Logger.getLogger(VentanaIngresarPelicula.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEscribirActionPerformed


